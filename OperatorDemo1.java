class OperatorDemo1
{
	public static void main (String[] arg)
	{
		System.out.println(5+5);
		System.out.println(22.5+36.4);
		System.out.println(29+34.65);

		System.out.println("the"+"skyxsea");
		System.out.println("only"+25);
		System.out.println(39+"boomboom");
		System.out.println(20+5+"java");
		System.out.println("java"+20+5);
		System.out.println("java"+(20+5));

		System.out.println('A'+'B');
		System.out.println('A'+10);
		System.out.println('C'+"java");
		System.out.println("java"+"developer");
	}
}