class DemoVariable4
{
	public static void main (String[] args)
	{
		int n1=10, n2=20, n3=30;
		char ch1='A', ch2='B', ch3='C';
		System.out.println(n1+"\t"+n2+"\t"+n3);
		System.out.println(ch1+"\t"+ch2+"\t"+ch3);
		System.out.println("------------------------------");
		System.out.println((n1+ch1)+"\t"+(n2+ch2)+"\t"+(n3+ch3));
	}
}